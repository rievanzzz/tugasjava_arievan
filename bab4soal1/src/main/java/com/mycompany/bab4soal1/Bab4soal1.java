public class Bab4soal1 {

    public static void main(String[] args) {
        // Dklarasi variable
        int angka1 = 30;
        int angka2 = 2;
        //operasi aritmatika
        int hasilPenambahan = angka1 + angka2;
        int hasilPengurangan = angka1 - angka2;
        int hasilPerkalian = angka1 * angka2;
        double hasilPembagian = (double) angka1 / angka2 ;
        
        //menampilkan hasil
        System.out.println("a. Hasil Pertambahan A + B = " + hasilPenambahan);
        System.out.println("a. Hasil Pengurangan A - B = " + hasilPengurangan);
        System.out.println("a. Hasil Perkalian A * B = " + hasilPerkalian);
        System.out.println("a. Hasil Pembagian A / B = " + hasilPembagian);
    }
}
